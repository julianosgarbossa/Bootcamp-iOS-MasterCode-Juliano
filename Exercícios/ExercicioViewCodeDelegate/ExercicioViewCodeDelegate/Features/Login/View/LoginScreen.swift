import UIKit

protocol LoginScreenDelegate: AnyObject {
    func didTapLoginButton()
    func didTapRegisterButton()
}

class LoginScreen: UIView {
    
    private weak var delegate: LoginScreenDelegate?
    
    func delegate(delegate: LoginScreenDelegate) {
        self.delegate = delegate
    }
    
    private lazy var topContentView: UIView = {
        let view = UIView()
        view.translatesAutoresizingMaskIntoConstraints = false
        
        // MARK: - Appearance
        view.backgroundColor = .clear
        
        return view
    }()
    
    private lazy var logoStackView: UIStackView = {
        let stack = UIStackView()
        stack.translatesAutoresizingMaskIntoConstraints = false
        
        // MARK: - Axis
        stack.axis = .vertical
        
        // MARK: - Distribution
        stack.distribution = .fill
        
        // MARK: - Alignment
        stack.alignment = .center
        // .leading
        // .trailing
        // .center
        
        // MARK: - Spacing
        stack.spacing = 4
        
        return stack
    }()
    
    private lazy var globeImageView: UIImageView = {
        let imageView = UIImageView()
        imageView.translatesAutoresizingMaskIntoConstraints = false
        
        // MARK: - Image
        imageView.image = UIImage(systemName: "globe.americas.fill")
        
        // MARK: - Content Mode
        imageView.contentMode = .scaleAspectFit
        imageView.clipsToBounds = true
        
        // MARK: - Tint
        imageView.tintColor = .white
        
        return imageView
    }()
    
    private lazy var logoLabel: UILabel = {
        let label = UILabel()
        
        // MARK: - Text
        label.text = "Countries"
        label.textColor = .white
        label.textAlignment = .center
        label.numberOfLines = 1
        
        // MARK: - Font
        label.font = UIFont.systemFont(ofSize: 42, weight: .bold)
        
        return label
    }()
    
    private lazy var bottomContentView: UIView = {
        let view = UIView()
        view.translatesAutoresizingMaskIntoConstraints = false
        
        // MARK: - Appearance
        view.backgroundColor = .white
        
        // MARK: - Layer
        view.layer.cornerRadius = 48
        view.layer.maskedCorners = [
            .layerMinXMinYCorner,
            .layerMaxXMinYCorner
        ]
        view.layer.masksToBounds = true
        
        return view
    }()
    
    private lazy var loginTitleLabel: UILabel = {
        let label = UILabel()
        label.translatesAutoresizingMaskIntoConstraints = false
        
        // MARK: - Text
        label.text = "Login"
        label.textColor = UIColor(red: 120/255, green: 120/255, blue: 120/255, alpha: 1)
        label.textAlignment = .center
        label.numberOfLines = 1
        
        // MARK: - Font
        label.font = UIFont.systemFont(ofSize: 24, weight: .semibold)
        
        return label
    }()
    
    private lazy var emailLabel: UILabel = {
        let label = UILabel()
        label.translatesAutoresizingMaskIntoConstraints = false
        
        // MARK: - Text
        label.text = "Email"
        label.textColor = UIColor(red: 120/255, green: 120/255, blue: 120/255, alpha: 1)
        label.textAlignment = .left
        label.numberOfLines = 1
        
        // MARK: - Font
        label.font = UIFont.systemFont(ofSize: 16, weight: .semibold)
        
        return label
    }()
    
    private lazy var emailTextField: UITextField = {
        let textField = UITextField()
        textField.translatesAutoresizingMaskIntoConstraints = false
        
        // MARK: - Text
        textField.placeholder = "exemplo@email.com"
        textField.textColor = .black
        textField.font = UIFont.systemFont(ofSize: 16)
        textField.textAlignment = .left
        
        // MARK: - Border Style
        textField.borderStyle = .none
        textField.leftView = UIView(frame: CGRect(x: 0, y: 0, width: 16, height: 0))
        textField.leftViewMode = .always
        textField.rightView = UIView(frame: CGRect(x: 0, y: 0, width: 16, height: 0))
        textField.rightViewMode = .always
        textField.layer.cornerRadius = 24
        textField.layer.borderWidth = 1
        textField.layer.borderColor = UIColor(red: 253/255, green: 155/255, blue: 1/255, alpha: 1).cgColor
        
        // MARK: - Keyboard
        textField.keyboardType = .emailAddress
        textField.returnKeyType = .next
        textField.autocapitalizationType = .none
        textField.autocorrectionType = .no
        textField.spellCheckingType = .no
        textField.isSecureTextEntry = false
        
        return textField
    }()
    
    private lazy var passwordLabel: UILabel = {
        let label = UILabel()
        label.translatesAutoresizingMaskIntoConstraints = false
        
        // MARK: - Text
        label.text = "Senha"
        label.textColor = UIColor(red: 120/255, green: 120/255, blue: 120/255, alpha: 1)
        label.textAlignment = .left
        label.numberOfLines = 1
        
        // MARK: - Font
        label.font = UIFont.systemFont(ofSize: 16, weight: .semibold)
        
        return label
    }()
    
    private lazy var passwordTextField: UITextField = {
        let textField = UITextField()
        textField.translatesAutoresizingMaskIntoConstraints = false
        
        // MARK: - Text
        textField.placeholder = "password"
        textField.textColor = .black
        textField.font = UIFont.systemFont(ofSize: 16)
        textField.textAlignment = .left
        
        // MARK: - Border Style
        textField.borderStyle = .none
        textField.leftView = UIView(frame: CGRect(x: 0, y: 0, width: 16, height: 0))
        textField.leftViewMode = .always
        textField.rightView = UIView(frame: CGRect(x: 0, y: 0, width: 16, height: 0))
        textField.rightViewMode = .always
        textField.layer.cornerRadius = 24
        textField.layer.borderWidth = 1
        textField.layer.borderColor = UIColor(red: 253/255, green: 155/255, blue: 1/255, alpha: 1).cgColor
        
        // MARK: - Keyboard
        textField.keyboardType = .default
        textField.returnKeyType = .done
        textField.autocapitalizationType = .none
        textField.autocorrectionType = .no
        textField.spellCheckingType = .no
        textField.isSecureTextEntry = true
        
        return textField
    }()
    
    private lazy var loginButton: UIButton = {
        let button = UIButton(type: .system)
        button.translatesAutoresizingMaskIntoConstraints = false
        
        // MARK: - Title
        button.setTitle("Entrar", for: .normal)
        button.setTitleColor(.white, for: .normal)
        button.setTitleColor(.lightGray, for: .highlighted)
        
        // MARK: - Font
        button.titleLabel?.font = UIFont.systemFont(ofSize: 16, weight: .semibold)
        button.titleLabel?.textAlignment = .center
        button.titleLabel?.numberOfLines = 1
        
        // MARK: - Background
        button.backgroundColor = UIColor(red: 253/255, green: 155/255, blue: 1/255, alpha: 1)
        
        // MARK: - Layer
        button.layer.cornerRadius = 24
        button.clipsToBounds = true
        
        // MARK: - Interaction
         button.addTarget(self, action: #selector(didTapLoginButton), for: .touchUpInside)
        
        return button
    }()
    
    private lazy var registerButton: UIButton = {
        let button = UIButton(type: .system)
        button.translatesAutoresizingMaskIntoConstraints = false
        
        // MARK: - Title
        button.setTitle("NÃ£o tem uma conta? Cadastre-se", for: .normal)
        button.setTitleColor(UIColor(red: 120/255, green: 120/255, blue: 120/255, alpha: 1), for: .normal)
        button.setTitleColor(.lightGray, for: .highlighted)
        
        // MARK: - Font
        button.titleLabel?.font = UIFont.systemFont(ofSize: 16, weight: .semibold)
        button.titleLabel?.textAlignment = .center
        button.titleLabel?.numberOfLines = 1
        
        // MARK: - Background
        button.backgroundColor = .white
        
        // MARK: - Layer
        button.layer.cornerRadius = 24
        button.layer.borderWidth = 1
        button.layer.borderColor = UIColor(red: 253/255, green: 155/255, blue: 1/255, alpha: 1).cgColor
        button.clipsToBounds = true
        
        // MARK: - Interaction
         button.addTarget(self, action: #selector(didTapRegisterButton), for: .touchUpInside)
        
        return button
    }()
    
    @objc
    private func didTapLoginButton(_ sender: UIButton) {
        delegate?.didTapLoginButton()
    }
    
    @objc
    private func didTapRegisterButton(_ sender: UIButton) {
        delegate?.didTapRegisterButton()
    }

    override init(frame: CGRect) {
        super.init(frame: frame)
        addVisualElements()
    }
    
    required init?(coder: NSCoder) {
        fatalError("init(coder:) has not been implemented")
    }
    
    private func addVisualElements() {
        backgroundColor = UIColor(red: 253/255, green: 155/255, blue: 1/255, alpha: 1.0)
        
        addSubview(topContentView)
        addSubview(logoStackView)
        logoStackView.addArrangedSubview(globeImageView)
        logoStackView.addArrangedSubview(logoLabel)
        addSubview(bottomContentView)
        addSubview(loginTitleLabel)
        addSubview(emailLabel)
        addSubview(emailTextField)
        addSubview(passwordLabel)
        addSubview(passwordTextField)
        addSubview(loginButton)
        addSubview(registerButton)
        
        configConstraints()
    }
    
    private func configConstraints() {
        NSLayoutConstraint.activate([
            topContentView.topAnchor.constraint(equalTo: topAnchor),
            topContentView.leadingAnchor.constraint(equalTo: leadingAnchor),
            topContentView.trailingAnchor.constraint(equalTo: trailingAnchor),
            topContentView.heightAnchor.constraint(equalTo: self.heightAnchor, multiplier: 0.4),
            
            logoStackView.centerXAnchor.constraint(equalTo: topContentView.centerXAnchor),
            logoStackView.centerYAnchor.constraint(equalTo: topContentView.centerYAnchor),
            
            globeImageView.heightAnchor.constraint(equalToConstant: 90),
            globeImageView.widthAnchor.constraint(equalToConstant: 90),
            
            bottomContentView.topAnchor.constraint(equalTo: topContentView.bottomAnchor),
            bottomContentView.leadingAnchor.constraint(equalTo: topContentView.leadingAnchor),
            bottomContentView.trailingAnchor.constraint(equalTo: topContentView.trailingAnchor),
            bottomContentView.bottomAnchor.constraint(equalTo: bottomAnchor),
            
            loginTitleLabel.topAnchor.constraint(equalTo: bottomContentView.topAnchor, constant: 24),
            loginTitleLabel.centerXAnchor.constraint(equalTo: bottomContentView.centerXAnchor),
            
            emailLabel.topAnchor.constraint(equalTo: loginTitleLabel.bottomAnchor, constant: 24),
            emailLabel.leadingAnchor.constraint(equalTo: bottomContentView.leadingAnchor, constant: 16),
            emailLabel.trailingAnchor.constraint(equalTo: bottomContentView.trailingAnchor, constant: -16),
            
            emailTextField.topAnchor.constraint(equalTo: emailLabel.bottomAnchor, constant: 12),
            emailTextField.leadingAnchor.constraint(equalTo: emailLabel.leadingAnchor),
            emailTextField.trailingAnchor.constraint(equalTo: emailLabel.trailingAnchor),
            emailTextField.heightAnchor.constraint(equalToConstant: 48),
            
            passwordLabel.topAnchor.constraint(equalTo: emailTextField.bottomAnchor, constant: 16),
            passwordLabel.leadingAnchor.constraint(equalTo: emailLabel.leadingAnchor),
            passwordLabel.trailingAnchor.constraint(equalTo: emailLabel.trailingAnchor),
            
            passwordTextField.topAnchor.constraint(equalTo: passwordLabel.bottomAnchor, constant: 12),
            passwordTextField.leadingAnchor.constraint(equalTo: passwordLabel.leadingAnchor),
            passwordTextField.trailingAnchor.constraint(equalTo: passwordLabel.trailingAnchor),
            passwordTextField.heightAnchor.constraint(equalTo: emailTextField.heightAnchor),
            
            loginButton.topAnchor.constraint(equalTo: passwordTextField.bottomAnchor, constant: 44),
            loginButton.leadingAnchor.constraint(equalTo: emailLabel.leadingAnchor),
            loginButton.trailingAnchor.constraint(equalTo: emailLabel.trailingAnchor),
            loginButton.heightAnchor.constraint(equalTo: emailTextField.heightAnchor),
            
            registerButton.topAnchor.constraint(equalTo: loginButton.bottomAnchor, constant: 16),
            registerButton.leadingAnchor.constraint(equalTo: emailLabel.leadingAnchor),
            registerButton.trailingAnchor.constraint(equalTo: emailLabel.trailingAnchor),
            registerButton.heightAnchor.constraint(equalTo: emailTextField.heightAnchor),
        ])
    }
}
